package com.yuanjun.vo;

public class Option {
	private String option_no ;
	private String option_title ;
	private String option_sn;
	public String getOption_no() {
		return option_no;
	}
	public void setOption_no(String option_no) {
		this.option_no = option_no;
	}
	public String getOption_title() {
		return option_title;
	}
	public void setOption_title(String option_title) {
		this.option_title = option_title;
	}
	public String getOption_sn() {
		return option_sn;
	}
	public void setOption_sn(String option_sn) {
		this.option_sn = option_sn;
	}
	

}
